public class LineCoordinates {

    int xStart;
    int yStart;
    int xEnd;
    int yEnd;

    public LineCoordinates(){}

    public LineCoordinates(int xStart, int yStart, int xEnd, int yEnd){
        this.xStart = xStart;
        this.yStart = yStart;
        this.xEnd = xEnd;
        this.yEnd = yEnd;
    }



}
